    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("KargoVeriTaban_PU");
        EntityManager em = emf.createEntityManager();
        Takip2 t = new Takip2();
        if (jRadioButton1.isSelected()) {

            try {
                t.setKargoId(id);
                t.setDurum("Merkezde.");

                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                String date = dateChooserCombo1.getText();
                java.sql.Date mydate = new java.sql.Date(sdf.parse(date).getTime());

                t.setTarih(mydate);
            } catch (ParseException ex) {
                Logger.getLogger(PersonelSayfası.class.getName()).log(Level.SEVERE, null, ex);
            }

          
        } else if (jRadioButton2.isSelected()) {

            try {
                t.setKargoId(id);
                t.setDurum("Yola çıktı.");
                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                String date = dateChooserCombo1.getText();
                java.sql.Date mydate = new java.sql.Date(sdf.parse(date).getTime());
                t.setTarih(mydate);
            } catch (ParseException ex) {
                Logger.getLogger(PersonelSayfası.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (jRadioButton3.isSelected()) {

            try {
                t.setKargoId(id);
                t.setDurum("Gümrükte.");
                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                String date = dateChooserCombo1.getText();
                java.sql.Date mydate = new java.sql.Date(sdf.parse(date).getTime());
                t.setTarih(mydate);
            } catch (ParseException ex) {
                Logger.getLogger(PersonelSayfası.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (jRadioButton4.isSelected()) {

            try {
                t.setKargoId(id);
                t.setDurum("Müşteriye ulaştı.");
                SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
                String date = dateChooserCombo1.getText();
                java.sql.Date mydate = new java.sql.Date(sdf.parse(date).getTime());
                t.setTarih(mydate);
            } catch (ParseException ex) {
                Logger.getLogger(PersonelSayfası.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        em.getTransaction().begin();
        em.persist(t);
        em.getTransaction().commit();
        ResetDtm();
        
        
        Query q = em.createQuery("SELECT k FROM Kargolar k WHERE k.kargoid=:Kargo_Id ");
        Query q1 = em.createQuery("SELECT t From Takip2 t WHERE t.kargoId=:KargoId");
        q.setParameter("Kargo_Id", id);
        q1.setParameter("KargoId", id);
        List<Kargolar> kargo = q.getResultList();
        List<Takip2> takip = q1.getResultList();
        for (Kargolar kargo1 : kargo) {
            for (Takip2 takip1 : takip) {
                dtm.setRowCount(0);
                dtm.addRow(new Object[]{kargo1.getKargoid(), kargo1.getAlantc(),
                    kargo1.getGönderentc(), takip1.getDurum(), takip1.getTarih()});
                }
        }

       
       
        em.close();
        emf.close();
    }//GEN-LAST:event_jButton1ActionPerformed

