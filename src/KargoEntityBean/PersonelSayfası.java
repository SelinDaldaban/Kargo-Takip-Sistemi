    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        File entityFile= new File ("Kisiler.txt");
        if (!entityFile.exists()) {
            try {
                entityFile.createNewFile();
                FileWriter fw= new FileWriter(entityFile.getAbsoluteFile());
                BufferedWriter bw= new BufferedWriter(fw);
                
                for (int i = 0; i < jTable2.getRowCount(); i++) {
                    for (int j = 0; j < jTable2.getColumnCount(); j++) {
                        bw.write(jTable2.getModel().getValueAt(i, j)+" ");
                    }
                    bw.write("\n");
                    bw.newLine();;
                }
                bw.close();
                fw.close();
                
            } catch (IOException ex) {
                Logger.getLogger(PersonelSayfası.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        JOptionPane.showMessageDialog(this, "Listelendi.");
    }//GEN-LAST:event_jButton4ActionPerformed

